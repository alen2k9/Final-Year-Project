package data.papillon;

public class PowerModelStatistics {

    private int id;
    private String modelId;
    private double power;
    private double stat1;
    private double stat2;
    private double stat3;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getModelId() {
        return modelId;
    }

    public void setModelId(String modelId) {
        this.modelId = modelId;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }

    public double getStat1() {
        return stat1;
    }

    public void setStat1(double stat1) {
        this.stat1 = stat1;
    }

    public double getStat2() {
        return stat2;
    }

    public void setStat2(double stat2) {
        this.stat2 = stat2;
    }

    public double getStat3() {
        return stat3;
    }

    public void setStat3(double stat3) {
        this.stat3 = stat3;
    }
}
